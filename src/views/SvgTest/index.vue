      textpath中可以包含tspan元素，可以进行位移、伸缩、旋转。 textpath属性：
      startOffset="num%":定义文本延路径排列时的起始位置。0%是起点，100%是终点。
      method="align|stretch":是否拉伸文字以适合整个路径。align表示不缩放按路径排列，stretch表示缩放以适应整个路径。
      spacing="auto|exact":指明空格的处理方式。
      exact表示将空格作为一个字符参与排列，auto不将空格计算在内，默认exact
      xlink="url":文字需要排版的路径。如果路径无效，会出错。
    </h3>

