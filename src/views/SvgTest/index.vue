    <h3>
      文字编排。svg支持不同的书写方向。因为不同语言的基点不同，基点指的是每个字符用来与其他字符对齐的点。基线就是基点的连线。另外，不同语言的方向也可能不同。
    </h3>
    <h3>
      css中有一个属性"writing-mode",用来定义文字的渲染方向。 writing-mode："lr-tb||rl-tb||tb-rl||"
      lr-tb:从左到右，从上到下(left-right-top-bottom),适用于大部分语言。
      rl-tb：从右到左，从上到下(right-left-top-bottom),通常用于希伯来文和阿拉伯文。
      tb-rl:从上到下，从右到左(top-bottom-right-left),通常用于亚洲国家，如我国书法类型作品。
      glyph-orientation-vertical:angle|auto|inherit
      这个参数只有文字竖排时生效，表示排列的实际方向与竖直方向的夹角，值可以是具体的角度，auto为默认值，inherit继承。
      glyph-orientation-horizonal:angle|inherit
      表示排列的实际方向与水平方向的夹角，值可以是具体的角度，缺省值为0
    </h3>

