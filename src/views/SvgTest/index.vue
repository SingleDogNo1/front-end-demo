    <h3>
      滤镜效果是svg中最具有感染力的效果，如果说之前的内容都属于2D平面范围内的话，那么滤镜可以使svg成为生动的3D形象。使用滤镜的方式同样是先定义，然后调用，不同的是滤镜的定义要比之前的其他效果定义复杂得多。
      filter元素常出现在defs内部，用于定义一个完整的滤镜效果；主要属性： id='id':引用时的标识；
      x="num":滤镜效果作用的矩形范围左上角的x坐标： y="num":滤镜效果作用的矩形范围左上角的y坐标：
      width="num":滤镜作用效果的矩形范围的宽度； height="num":滤镜作用效果的矩形范围的高度；
      filterUnits='userSpaceOnUse|ObjectBoundingBox':指明滤镜作用区域坐标值的长度单位。userSpaceOnUse表示长度单位取决于引用本filter元素所在的用户的坐标系。ObjectBoundingBox表示长度单位以引用本filter元素的图像对象的矩形外框为参考坐标系，也是该属性的缺省值。
      primitiveUnits='userSpaceOnUse|ObjectBoundingBox':指明本滤镜中各种基本滤镜变换效果所使用的坐标的长度单位。值与1filterUnits属性的含义相同。
      fliterRes="num|num":filter元素特有，他指明了本滤镜效果使用时的图像的分辨率。如果不指明，则对于显示屏，缺省值为屏幕分辨率；对于印刷作品，缺省值为400dpi，如果只有一个值，表示x方向和y方向的分辨率相同。
      基本滤镜变换是每一步滤镜变换的统称，这些变换作为filter的子元素存在。一般以fe开头，如feGaussianBlur、feOffset等。其中有些只是含有属性的空元素，有些仍然包含子元素。他们有共同的属性：
      x="num":滤镜效果作用的矩形范围左上角的x坐标，默认-10%；
      y="num":滤镜效果作用的矩形范围左上角的y坐标，默认-10%；
      width="num":滤镜作用效果的矩形范围的宽度，默认120%；
      height="num":滤镜作用效果的矩形范围的高度默认120%；
      result="filter-primitrve-result-name":指明基本滤镜变换的结果，每一步变换都会产生一个结果值，以供接下来使用；
      in="SourceGraphic | SourceAlpha | BackgroundImage | BackgroundAlpha | FillPaint | StrokePaint"
      SourceGraphic:引用filter元素的可视化对象。如一个rect元素引用了本filter，则该参数代表这个矩形的可视部分
      SourceAlpha：引用filter元素的可视化对象的透明度。所有可视化元素都是作为4通道图像存在的。SourceGraphic表示前三个rgb通道，sourceAlpha表示透明度通道。
      BackgroundImage：引用filter元素的可视化对象的显示区域(包括背景)。如果引用本滤镜的元素
      下面重叠绘有不同的元素，使用BackgroundImage将对所有元素添加filter效果，而不仅仅是引用这个filter的对象。
      backgroundAlpha：与上一个属性类似，透明度将作用于所有元素。
      fillPaint：指引用filter元素的对象的填充区域。 strokePaint：指引用filter元素的对象的描边区域。
      另外，在使用背景时，用于包裹图像的元素还可以在style属性中规定一个"enable-background"参数，用来指明是否允许使用图像背景。
      值可以取俩个："new| accumulate"。new表示包含元素的子元素可以自由的访问背景(及允许使用
      BackgroundImage和BackgroundAlpha)。accumulate表示不允许，同时也是该属性的缺省值。
    </h3>

    <h3>
      光源和光照模式：svg中，光源元素并不独立作用于被照射的物体，而是作为光照模式元素的子元素而存在，光照模式指明了所含有的光源子元素照射时的反射模式，如漫反射、高光等。
      svg中光源有三种：平行光、点光源和聚光灯。 feDistantLight:平行光，属性有俩个
      azimuth：光线的方位角(光线所在的直线在xy平面上的投影与x轴正方向的夹角，单位deg)
      elevation：光线的仰角(光线本身与其在xy平面上的投影的夹角)
      fePointLight:点光源(光源是一个点，光线朝四周均匀发散)，属性有三个： x="num":光源位置的x坐标；
      y="num":光源位置的y坐标； z="num":光源位置的z坐标；
      feSpotLight:聚光灯(点光源发射的光集中在一个圆锥范围内，光源刚好是圆锥的顶点)，属性如下：
      x="num":光源位置的x坐标； y="num":光源位置的y坐标； z="num":光源位置的z坐标；
      pointsAtX="num":圆锥底部的圆心的x坐标; pointsAtY="num":圆锥底部的圆心的y坐标;
      pointsAtZ="num":圆锥底部的圆心的z坐标; specularExponent="num":知名光线的聚焦程度；
      limitingConeAngle="num"：指明圆锥的发散程度，值为圆锥轴线与母线的夹角(圆锥锥角的一半)，如果不说明该值，会无限发散，变成了点光源
      光照模式：有镜面反射和漫反射俩种，
      feDiffuseLighting：漫反射。该元素可以包含多个定义光源的元素，属性如下：
      surfaceScale="num":透明度到Z坐标(高度)转换的比例因子。透明度为1时转换后的Z坐标极为属性的值。可根据需求设置。
      diffuseConstant="num"：指明漫反射系数，可以是任何非0的值，通常为1
      feDiffuseLighting元素一般施加到透明度分布有过渡情况的图像对象，这样透明度转换后z坐标分布有过渡，而不是黑白分明，光照的效果也会更明显。
      feSpecularLighting:镜面反射。有强烈的立体感。属性如下：
      surfaceScale="num":透明度到Z坐标(高度)转换的比例因子。透明度为1时转换后的Z坐标极为属性的值。可根据需求设置。
      specularConstant="num"：指明镜面反射系数，可以是任何非0的值。
      specularExponent="num":镜面反射的范围的广泛程度，值在0-128之间，值越大越接近镜面反射，如果设置为0，几乎等同于漫反射
      此外，这俩种元素都可以设置光线颜色，通过style="lighting-color:red";
    </h3>

